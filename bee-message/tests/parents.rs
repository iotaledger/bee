#[test]
fn unpack_invalid_length_less_than_min() {
    let bytes = vec![0u8, 0b00000000];

    assert!(matches!(
        ParentsBlock::unpack_from_slice(bytes),
        Err(UnpackError::Packable(MessageUnpackError::Validation(
            ValidationError::InvalidParentsCount(0)
        ))),
    ));
}

